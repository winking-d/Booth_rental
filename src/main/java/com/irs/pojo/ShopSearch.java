package com.irs.pojo;

public class ShopSearch {
    private String shopNumber;
    private String shopType;
    private String shopSate;
    private String shopSizeMin;
    private String shopSizeMax;

    public String getShopNumber() {
        return shopNumber;
    }

    public void setShopNumber(String shopNumber) {
        this.shopNumber = shopNumber;
    }

    public String getShopType() {
        return shopType;
    }

    public void setShopType(String shopType) {
        this.shopType = shopType;
    }

    public String getShopSate() {
        return shopSate;
    }

    public void setShopSate(String shopSate) {
        this.shopSate = shopSate;
    }

    public String getShopSizeMin() {
        return shopSizeMin;
    }

    public void setShopSizeMin(String shopSizeMin) {
        this.shopSizeMin = shopSizeMin;
    }

    public String getShopSizeMax() {
        return shopSizeMax;
    }

    public void setShopSizeMax(String shopSizeMax) {
        this.shopSizeMax = shopSizeMax;
    }
}
